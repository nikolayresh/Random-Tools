namespace RandomTools.Core
{
	/// <summary>
	/// Specifies units of time for delays or durations.
	/// </summary>
	public enum TimeUnit
	{
		/// <summary>
		/// Represents milliseconds (1/1,000 of a second).
		/// </summary>
		Millisecond,

		/// <summary>
		/// Represents seconds.
		/// </summary>
		Second,

		/// <summary>
		/// Represents minutes.
		/// </summary>
		Minute
	}
}